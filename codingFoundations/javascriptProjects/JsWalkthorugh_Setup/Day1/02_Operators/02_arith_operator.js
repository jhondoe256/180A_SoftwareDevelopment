let d= 25;
//todo: Increment(++) -> counts up by one
console.log(d); //25

//todo: count up by one, ++(before the initial value)
console.log(++d); //26  d+=1

//todo: count up by one, (after the initial value)++
console.log(d++); //25  d+=1

let c = 3;
//todo: subtract down by one, --(before the initial value)
console.log(c--); //3

//todo: subtract down by one, (after the initial value)--
console.log(--c); //1