//Comparison Operators
// compare operand a to operand b

let a = 7; //operand a 

let b = 10; //operand b

//todo: relational operators
// check if one operand is greater than another
// this return a boolean (true/false)
console.log(a > b);  //false   is a greater than b
console.log(a > 0);  //true    greater than
console.log(a <= 7);   //true  less than or equal to 

//todo: Equality Operator

//check if a 'is absolutely' equal to
//strict - equality
console.log(a === 7);

//check if a 'is absolutely NOT' equal to
console.log(a !== 7);
